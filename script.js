// adds two numbers
function add(num1, num2){
    return num1 + num2;
}

// subtracts two numbers
function subtract(num1, num2){
    return num1 - num2;
}

// multiplies two numbers
function multiply(num1, num2){
    return num1 * num2;
}

// divides two numbers
function divide(num1, num2){
    return num1 / num2;
}